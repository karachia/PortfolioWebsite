resume.DisplayWork = function(){
    var counter = 1;
    resume.work.forEach(function(item) {
        HTMLwork = HTMLworkTemplate;

        var HTMLskills = "";
        var HTMLtags = "";
        var HTMLinfo = "";
        for  (var skill of item.skills){
           HTMLskills += ('<span class="skill">' + skill + ' </span>');
        //    console.log(skill);
        }

        for (var tag of item.tags){
            // console.log(tag);
            HTMLtags += (" " + tag);
            
        }

        for (var line of item.info){
            // console.log(line);
            HTMLinfo += ("<li>" + line + "</li>");
        }


         //web links
         if(item.websitelink == "" || item.websitelink == null){
            HTMLwork = HTMLwork.replace("%websitelink%", "");
            HTMLwork = HTMLwork.replace("%websitelink%", "");
            HTMLwork = HTMLwork.replace("%websitetext%", "");

        }
        else{
            HTMLwork = HTMLwork.replace("%websitelink%", item.websitelink);
            HTMLwork = HTMLwork.replace("%websitelink%", item.websitelink);
            HTMLwork = HTMLwork.replace("%websitetext%", item.websitetext);
        }

        if(item.logo == null){
            HTMLwork = HTMLwork.replace("%logo%", "");
        }
        else{
            HTMLwork = HTMLwork.replace("%logo%", item.logo);
        }
        HTMLwork = HTMLwork.replace("%position%", item.position);
        HTMLwork = HTMLwork.replace("%company%", item.company);
        HTMLwork = HTMLwork.replace("%year%", item.year);
        HTMLwork = HTMLwork.replace("%location%", item.location);
        HTMLwork = HTMLwork.replace("%tags%", HTMLtags);
        HTMLwork = HTMLwork.replace("%skills%", HTMLskills);
        HTMLwork = HTMLwork.replace("%info%", HTMLinfo);
        if(counter != resume.work.length){
            HTMLwork = HTMLwork.replace("%line%", '<hr class="resume-line my-3">');            
        }
        else{
            HTMLwork = HTMLwork.replace("%line%", ''); 
        }
        $("#work").append(HTMLwork);
        counter++;
    });

};

